#include "ft_printf.h"

int	isflag(char c)
{
	return (c == 'd' || c == 'i' || c == 'x' || c == 'X' || c == 'c'
		|| c == 's' || c == '%' || c == 'p' || c == 'u');
}

int	printf_atoi(char **s)
{
	int	value;

	value = 0;
	while (**s >= '0' && **s <= '9')
	{
		value = value * 10 + (**s - '0');
		*s += 1;
	}
	*s -= 1;
	return (value);
}
